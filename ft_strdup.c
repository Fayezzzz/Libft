#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*dest;
	size_t		i;

	i = ft_strlen(str);
	dest = malloc((i + 1) * sizeof(char));
	if (!dest)
		return (NULL);
	i = 0;
	while (str[i])
	{
		dest[i] = str[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
